package Practice.Exercises.Week02.maths;

public class PowTable {
    public static void main(String[] args) {
        System.out.println(" a  b  pow(a, b)");
        int a1 = 1, b1 = 2;
        int power1 = (int) Math.pow(a1, b1);
        System.out.println(a1 + "  " + b1 + "  " + power1);
        int a2 = 2, b2 = 3;
        int power2 = (int) Math.pow(a2, b2);
        System.out.println(a2 + "  " + b2 + "  " + power2);
        int a3 = 3, b3 = 4;
        int power3 = (int) Math.pow(a3, b3);
        System.out.println(a3 + "  " + b3 + "  " + power3);
    }
}

